// Find the vertexs and edges based on the vertex and edge in each cell
void findVertexEdges(std::vector<std::vector<double>> &cellVertex, std::map<std::vector<double>, int> &temp, std::vector<std::vector<double>> &vertexs, std::vector<std::vector<double>> &cellNormals,
    std::vector<std::vector<double>> &norms, std::vector<std::vector<int>> &edges)
{
    // n is the number of triangles for each cell
    int n = int(cellVertex.size())/3;
    
    // index1, index2, index3 are the indexs of three vertexs of a triangle in vector vertexs
    int index1 = 0;
    int index2 = 0;
    int index3 = 0;
    
    for(int i = 0; i < n; i++)
    {
        // Search the current vertex1 in temp map
        auto it1 = temp.find(cellVertex[0 + i*3]);
        // if it didn't exist, push it into vector vertexs and map temp, and index equals to the length of vector
        if(it1 == temp.end())
        {
            // If this is a new vertex that is not in vertexs array, just put it into this array and norms array
            vertexs.push_back(cellVertex[0 + i*3]);
            // For norms array, the last column records the number of faces shared this vertex
            norms.push_back({cellNormals[0 + i*3][0], cellNormals[0 + i*3][1], cellNormals[0 + i*3][2], 1});
            // Put this vertex into temp hashmap as key, and value is the order
            temp.insert(std::pair<std::vector<double>, int>(cellVertex[0 + i*3], int(vertexs.size())));
            // Get the index of this vertex in vertexs array, started from 1.
            index1 = int(vertexs.size());
        }
        // if it exists, get the index
        else
        {
            // Get the index from temp map
            index1 = it1->second;
            // Get sum of norms with shared faces
            norms[index1-1] = {norms[index1-1][0] + cellNormals[0 + i*3][0], norms[index1-1][1] + cellNormals[0 + i*3][1],
                norms[index1-1][2] + cellNormals[0 + i*3][2], norms[index1-1][3] + 1};
        }
        // Search the current vertex2 in temp map
        auto it2 = temp.find(cellVertex[1 + i*3]);
        if(it2 == temp.end())
        {
            // If this is a new vertex that is not in vertexs array, just put it into this array and norms array
            vertexs.push_back(cellVertex[1 + i*3]);
            // For norms array, the last column records the number of faces shared this vertex
            norms.push_back({cellNormals[1 + i*3][0], cellNormals[1 + i*3][1], cellNormals[1 + i*3][2], 1});
            // Put this vertex into temp hashmap as key, and value is the order
            temp.insert(std::pair<std::vector<double>, int>(cellVertex[1 + i*3], int(vertexs.size())));
            // Get the index of this vertex in vertexs array, started from 1.
            index2 = int(vertexs.size());
        }
        else
        {
            // Get the index from temp map
            index2 = it2->second;
            // Get sum of norms with shared faces
            norms[index2-1] = {norms[index2-1][0] + cellNormals[1 + i*3][0], norms[index2-1][1] + cellNormals[1 + i*3][1],
                norms[index2-1][2] + cellNormals[1 + i*3][2], norms[index2-1][3] + 1};
        }
        // Search the current vertex3 in temp map
        auto it3 = temp.find(cellVertex[2 + i*3]);
        if(it3 == temp.end())
        {
            // If this is a new vertex that is not in vertexs array, just put it into this array and norms array
            vertexs.push_back(cellVertex[2 + i*3]);
            // For norms array, the last column records the number of faces shared this vertex
            norms.push_back({cellNormals[2 + i*3][0], cellNormals[2 + i*3][1], cellNormals[2 + i*3][2], 1});
            // Put this vertex into temp hashmap as key, and value is the order
            temp.insert(std::pair<std::vector<double>, int>(cellVertex[2 + i*3], int(vertexs.size())));
            // Get the index of this vertex in vertexs array, started from 1.
            index3 = int(vertexs.size());
        }
        else
        {
            // Get the index from temp map
            index3 = it3->second;
            // Get sum of norms with shared faces
            norms[index3-1] = {norms[index3-1][0] + cellNormals[2 + i*3][0], norms[index3-1][1] + cellNormals[2 + i*3][1],
                norms[index3-1][2] + cellNormals[2 + i*3][2], norms[index3-1][3] + 1};
        }
        
        // push index1, index2, index3 to edges as a face
        edges.push_back({index1, index2, index3});
    }
}


// Normalize a vector
void nomalize(std::vector<double> &norm, std::vector<double> &normalizeNorm)
{
    // Calculate the length of this vector
    double vectorLen = std::sqrt(norm[0]*norm[0] + norm[1]*norm[1] + norm[2]*norm[2]);
    // Normalization
    normalizeNorm = {norm[0]/vectorLen, norm[1]/vectorLen, norm[2]/vectorLen};
}


// Calculate normal vector for a cell
void calcNormal(std::vector<std::vector<double>> &cellVertex,  std::vector<std::vector<double>> &cellNormals)
{
    // The number of faces, and each faces has three vertexes
    int n = int(cellVertex.size())/3;
    
    for(int i = 0; i < n; i++)
    {
        // Three vertexs of a face
        std::vector<double> p1 = cellVertex[0 + i*3];
        std::vector<double> p2 = cellVertex[1 + i*3];
        std::vector<double> p3 = cellVertex[2 + i*3];
        
        // Two vectors of a face
        std::vector<double> v1 = {p2[0] - p1[0],  p2[1] - p1[1], p2[2] - p1[2]};
        std::vector<double> v2 = {p3[0] - p1[0],  p3[1] - p1[1], p3[2] - p1[2]};
        
        // Calculate normal vector
        std::vector<double> normal = {v1[1]*v2[2] - v1[2]*v2[1], v1[2]*v2[0] - v1[0]*v2[2], v1[0]*v2[1] - v1[1]*v2[0]};
        
        // For these three vertexs in a same face, they have same normal vector
        cellNormals.push_back(normal);
        cellNormals.push_back(normal);
        cellNormals.push_back(normal);
    }
}


